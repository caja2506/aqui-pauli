/** Obtener lista de provincias */
export function getProvincias() {
  return Object.keys(CR_TERRITORIAL);
}

/** Obtener cantones de una provincia */
export function getCantones(provincia) {
  return CR_TERRITORIAL[provincia] ? Object.keys(CR_TERRITORIAL[provincia]) : [];
}

/** Obtener distritos de un cantón en una provincia */
export function getDistritos(provincia, canton) {
  return CR_TERRITORIAL[provincia]?.[canton] || [];
}

/** Buscar por código postal → { provincia, canton, distrito, codigo } */
export function buscarPorCodigoPostal(codigo) {
  if (!codigo || codigo.length < 5) return null;
  const clean = codigo.replace(/\D/g, '').slice(0, 5);

  for (const [provincia, cantones] of Object.entries(CR_TERRITORIAL)) {
    for (const [canton, distritos] of Object.entries(cantones)) {
      for (const d of distritos) {
        if (d.codigo === clean) {
          return { provincia, canton, distrito: d.distrito, codigo: d.codigo };
        }
      }
    }
  }
  return null;
}

export default CR_TERRITORIAL;
